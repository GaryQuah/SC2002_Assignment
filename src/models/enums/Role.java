package models.enums;

/**
 * Enum representing different user roles in a system.
 *
 * This enum defines roles such as Patient, Doctor, Pharmacist, and Administrator,
 * which can be used to determine user access levels or permissions within an application.
 */
public enum Role {

    /** Role for a patient in the system. */
    Patient,

    /** Role for a doctor in the system. */
    Doctor,

    /** Role for a pharmacist in the system. */
    Pharmacist,

    /** Role for an administrator in the system. */
    Administrator;
}
