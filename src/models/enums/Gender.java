package models.enums;

/**
 * Enum representing different genders.
 *
 * This enum includes standard genders like Male and Female, as well as a category for "Others"
 * and an "UNKNOWN" value to handle unspecified or unknown gender information.
 */
public enum Gender {

    /** Male gender. */
    Male,

    /** Female gender. */
    Female,

    /** Other gender. */
    Others,

    /** Unknown gender. */
    UNKNOWN; // Added UNKNOWN value
}
